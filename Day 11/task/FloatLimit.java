public class FloatLimit {
    public static void main(String[] args) {
        Integer a = 127;
        Integer obj = 127;
        System.out.println(a == obj); 

        Integer b = 200;
        Integer obj1 = 200;
        System.out.println(b == obj1);

        Float k = 127f;
        Float m = 127f;
        System.out.println(k == m); 
}}